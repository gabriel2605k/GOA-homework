# 3) შექმენით ფუნქცია რომელსაც არგუმენტად გადაეცემა რიცხვები
# თ სავსე სია შემდეგ კი ფუნქციამ უნდა დააბრუნოს უმცირესი რიცხვი ამ სიიდან
def უდიდესი_რიცხვის_მოძებნა(რიცხვები):
    if not რიცხვები: 
        return None 
    უდიდესი = რიცხვები[0]  
    for რიცხვი in რიცხვები:
        if რიცხვი < უდიდესი:
            უდიდესი = რიცხვი 
    return უდიდესი 

სია = [3, 5, 1, 8, 2]
print(უდიდესი_რიცხვის_მოძებნა(სია))  
